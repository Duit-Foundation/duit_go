package duit_widget

import (
	"github.com/Duit-Foundation/duit_go/v4/pkg/duit_attributes"
	"github.com/Duit-Foundation/duit_go/v4/pkg/duit_props"
	"github.com/Duit-Foundation/duit_go/v4/pkg/duit_core"
)

func PhysicalModel[TColor duit_props.Color](attributes *duit_attributes.PhysicalModelAttributes[TColor], id string, controlled bool, child *duit_core.DuitElementModel) *duit_core.DuitElementModel {
	return new(duit_core.DuitElementModel).CreateElement(duit_core.PhysicalModel, id, "", attributes, nil, controlled, 1, nil, child)
} 