package duit_widget

import (
	"github.com/Duit-Foundation/duit_go/v4/pkg/duit_attributes"
	"github.com/Duit-Foundation/duit_go/v4/pkg/duit_core"
)

func AnimatedPhysicalModel(attributes *duit_attributes.AnimatedPhysicalModelAttributes, id string, child *duit_core.DuitElementModel) *duit_core.DuitElementModel {
	checkAttributes(attributes)
	return new(duit_core.DuitElementModel).CreateElement(duit_core.AnimatedPhysicalModel, id, "", attributes, nil, true, 1, nil, child)
} 